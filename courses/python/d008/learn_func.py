# Function

""" What is Function?
type : syntax
how to use : //
def func_name(var1_name(:var1_type), var2_name = var2_value, .../)
   action code 
   (return output_value)
func_name(var1, var2_name = var2, .../)
//
input :
  - func_name : function name
  - var_name : variable name
  - var_name = var_value: variable name with value
  - action code : code that you want to run with variables
output :
  - if action code meets return : return the output_value as output
  - if there is no return : return None
spetial use :
  - you could use *arg and **kwarg to revieve multiple values with unknown quantity
"""
# function : function that is independent with every class
# method : function that only can be used with specific class


